    @Override
    public void keyPressed(KeyEvent e) {
        switch(e.getKeyCode()) {
            //Right
            case KeyEvent.VK_RIGHT:
                lastKey = 1;
                break;

            //Left
            case KeyEvent.VK_LEFT:
                lastKey = 2;
                break;

            //Up
            case KeyEvent.VK_UP:
                lastKey = 3;
                break;

            case KeyEvent.VK_DOWN:
                lastKey = 4;
                break;
        }
    }

