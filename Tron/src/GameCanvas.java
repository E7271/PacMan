    /**
     * Handles all rendering
     * @param g the graphics object, given
     */
    public void paint(Graphics g) {
        super.paint(g);
        Graphics2D g2d = (Graphics2D) g;
        // TODO paint graphics here, using for loops

        g2d.setColor(Color.YELLOW);
        for (int row = 0; row < grid.length; row++) {
            for (int col = 0; col < grid[row].length; col++) {

                if (grid[row][col] == 0) {
                    g2d.setColor(Color.YELLOW);
                    // Pixel coordinates
                    int x_coords = 25 * col;
                    int y_coords = 25 * row;

                    // Block dimensions
                    int width = 25;
                    int height = 25;

                    g2d.fillRect(x_coords, y_coords, width, height);
                }
                if (grid[row][col] == 1) {
                    g2d.setColor(Color.BLACK);
                    // Pixel coordinates
                    int x_coords = 25 * col;
                    int y_coords = 25 * row;

                    // Block dimensions
                    int width = 25;
                    int height = 25;

                    g2d.fillRect(x_coords, y_coords, width, height);
                }
                if (grid[row][col] == 3) {
                    g2d.setColor(Color.WHITE);
                    // Pixel coordinates
                    int x_coords = 25 * col;
                    int y_coords = 25 * row;

                    // Block dimensions
                    int width = 25;
                    int height = 25;

                    g2d.fillRect(x_coords, y_coords, width, height);
                }

                if (grid[row][col] == 8) {
                    g2d.setColor(Color.ORANGE);
                    // Pixel coordinates
                    int x_coords = 25 * col;
                    int y_coords = 25 * row;

                    // Block dimensions
                    int width = 25;
                    int height = 25;

                    g2d.fillRect(x_coords, y_coords, width, height);
                }

                if (grid[row][col] == 9) {
                    g2d.setColor(Color.PINK);
                    // Pixel coordinates
                    int x_coords = 25 * col;
                    int y_coords = 25 * row;

                    // Block dimensions
                    int width = 25;
                    int height = 25;

                    g2d.fillRect(x_coords, y_coords, width, height);
                }



                g2d.setColor(Color.WHITE);
                g2d.fillRect(((Characters.playerPositionX * 25) + 3), ((Characters.playerPositionY * 25) + 3), Characters.playerWidth, Characters.playerHeight);

                g2d.setColor(Color.BLUE);
                g2d.fillRect(((Characters.enemyPositionX * 25) +3), ((Characters.enemyPositionY * 25) + 3), Characters.enemyWidth, Characters.enemyHeight);


            }
        }

    }
    @Override
    public void keyPressed(KeyEvent e) {
        switch(e.getKeyCode()) {
            //Right
            case KeyEvent.VK_RIGHT:
                lastKey = 1;
                break;

            //Left
            case KeyEvent.VK_LEFT:
                lastKey = 2;
                break;

            //Up
            case KeyEvent.VK_UP:
                lastKey = 3;
                break;
            //Down
            case KeyEvent.VK_DOWN:
                lastKey = 4;
                break;
        }

        switch(e.getKeyCode()) {
            //Right
            case KeyEvent.VK_D:
                enemyKey = 1;
                break;

            //Left
            case KeyEvent.VK_A:
                enemyKey = 2;
                break;

            //Up
            case KeyEvent.VK_W:
                enemyKey = 3;
                break;
            //Down
            case KeyEvent.VK_S:
                enemyKey = 4;
                break;
        }
    }
    @Override
    public void keyTyped(KeyEvent e) { }
    @Override
    public void keyReleased(KeyEvent e) { }
